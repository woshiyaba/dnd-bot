"""战斗领域枚举。

集中定义参战者、攻击、状态、战斗流程相关的封闭花名册。
字段取值沿用 docs/原始数据.md 与 docs/战斗 的中文用语，
枚举值即为序列化后写进卡面/状态的字符串，便于直接落库与前端展示。
"""

from __future__ import annotations

from enum import Enum


class StrEnum(str, Enum):
    """值即字符串的枚举基类（Python 3.13 自带 enum.StrEnum，这里显式声明以兼容旧逻辑）。"""

    def __str__(self) -> str:  # pragma: no cover - 仅为可读
        return str(self.value)


class 属性(StrEnum):
    """六项基础属性。检定/攻击/豁免的调整值都由这六项现算。"""

    力量 = "力量"
    敏捷 = "敏捷"
    体质 = "体质"
    智力 = "智力"
    感知 = "感知"
    魅力 = "魅力"


class 伤害类型(StrEnum):
    """伤害类型，本身不带规则，由抗性/易伤等机制参考。"""

    挥砍 = "挥砍"
    穿刺 = "穿刺"
    钝击 = "钝击"
    强酸 = "强酸"
    冷冻 = "冷冻"
    火焰 = "火焰"
    力场 = "力场"
    闪电 = "闪电"
    黯蚀 = "黯蚀"
    毒素 = "毒素"
    心灵 = "心灵"
    光耀 = "光耀"
    雷鸣 = "雷鸣"


class 射程(StrEnum):
    """攻击射程，配合区域判断够不够得着。"""

    近战 = "近战"
    远程 = "远程"


class 存活状态(StrEnum):
    """本版只区分能否继续行动；不做死亡豁免。"""

    正常 = "正常"
    倒下 = "倒下"


class 状态类型(StrEnum):
    """起步支持的状态枚举，机械效果见 docs/原始数据.md 1.8。"""

    倒地 = "倒地"
    中毒 = "中毒"
    束缚 = "束缚"
    眩晕 = "眩晕"
    持续伤害 = "持续伤害"


class 阵营(StrEnum):
    """判胜负用的阵营划分。"""

    玩家 = "玩家"
    敌人 = "敌人"


class 战斗阶段(StrEnum):
    """供调试与前端展示当前处于流程的哪一步。"""

    初始化 = "初始化"
    判突袭 = "判突袭"
    掷先攻 = "掷先攻"
    回合中 = "回合中"
    结算 = "结算"
    结束 = "结束"


class 战斗结果(StrEnum):
    """条件边据此决定走"下一位"还是"结算"。"""

    进行中 = "进行中"
    玩家胜 = "玩家胜"
    玩家败 = "玩家败"


class 行动类型(StrEnum):
    """声明行动节点产出的动作种类。"""

    攻击 = "攻击"
    技能 = "技能"
    道具 = "道具"
    创意 = "创意"
    移动 = "移动"
    放弃 = "放弃"


class 中断类型(StrEnum):
    """需要玩家报骰/选择的中断点，见 docs/战斗/03-中断交互协议.md。"""

    掷先攻 = "掷先攻"
    声明行动 = "声明行动"
    攻击检定 = "攻击检定"
    伤害掷骰 = "伤害掷骰"
    豁免检定 = "豁免检定"
    属性检定 = "属性检定"
